// Métodos para lidar com os elementos dos arrays

const alunos = ['André', 'Carlos', 'Adriano', 'Juliano'];
console.log(alunos);

// Imprimir um elemento
console.log(alunos[0]);

// Adicionar elementos ao final do array
alunos.push('Alex') 
console.log(alunos);

// Adicionar elementos ao início do array
alunos.unshift('Vagner')
console.log(alunos);

// Remover elementos do final do array
alunos.pop()
console.log(alunos);

// Remover elementos do início do array
alunos.shift()
console.log(alunos);